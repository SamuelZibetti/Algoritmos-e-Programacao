x = int(input("Digite um valor: "))
y = int(input("Digite outro valor: "))

soma = x + y
subtracao = x - y
divisao = x / y
multiplicacao = x * y

print("Resultados: ")
print(f"{x} + {y} =", soma)
print(f"{x} - {y} =", subtracao)
print(f"{x} / {y} =", divisao)
print(f"{x} * {y} =", multiplicacao)